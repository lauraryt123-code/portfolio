// ===== OTWIERANIE PROJEKTU =====
function openProject(projectId) {
    const project = projects[projectId];

     // Pokaż przycisk X
    document.getElementById('modalCloseBtn').style.display = 'flex';
    
    document.getElementById('projectTitle').textContent = project.title;
    // ... reszta kodu bez zmian

    document.getElementById('projectTitle').textContent = project.title;
    document.getElementById('projectCategory').textContent = project.category;
    document.getElementById('projectYear').textContent = project.year;
    document.getElementById('projectDescription').innerHTML = project.description;
    
    const imagesContainer = document.getElementById('modalImages');
    imagesContainer.innerHTML = '';
    
    project.images.forEach((imageSrc, index) => {
        const img = document.createElement('img');
        img.src = imageSrc;
        img.alt = `${project.title} - zdjęcie ${index + 1}`;
        imagesContainer.appendChild(img);
    });
    
    document.getElementById('projectModal').classList.add('active');
    document.body.style.overflow = 'hidden';
    document.getElementById('projectModal').scrollTop = 0;
}

// ===== ZAMYKANIE PROJEKTU =====
function closeProject() {
    document.getElementById('projectModal').classList.remove('active');
    document.body.style.overflow = 'auto';
    // Ukryj przycisk X
    document.getElementById('modalCloseBtn').style.display = 'none';
}

document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape') closeProject();
});

// ===== CHOWANIE MENU PRZY SCROLLU =====
let lastScroll = 0;
const navbar = document.getElementById('navbar');
const scrollThreshold = 100;

window.addEventListener('scroll', () => {
    const currentScroll = window.pageYOffset;
    
    if (currentScroll <= scrollThreshold) {
        navbar.classList.remove('hidden');
        lastScroll = currentScroll;
        return;
    }
    
    if (currentScroll > lastScroll && currentScroll > scrollThreshold) {
        navbar.classList.add('hidden');
    } else if (currentScroll < lastScroll) {
        navbar.classList.remove('hidden');
    }
    
    lastScroll = currentScroll;
});

// ===== PŁYNNE PRZEWIJANIE DO SEKCJI =====
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        
        if (target) {
            const offsetTop = target.offsetTop - 100;
            window.scrollTo({
                top: offsetTop,
                behavior: 'smooth'
            });
        }
    });
});

// ===== ANIMACJA POJAWIANIA PROJEKTÓW =====
const observerOptions = {
    threshold: 0.1,
    rootMargin: '0px 0px -50px 0px'
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry, index) => {
        if (entry.isIntersecting) {
            setTimeout(() => {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
            }, index * 100);
        }
    });
}, observerOptions);

document.querySelectorAll('.gallery-item').forEach(item => {
    item.style.opacity = '0';
    item.style.transform = 'translateY(30px)';
    item.style.transition = 'all 0.6s ease';
    observer.observe(item);
});

